package com.dfd.plugin.generator.code.ui;

public class MathLine {

    private int a;
    private int b;
    private int c;

    public MathLine(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getC() {
        return c;
    }
}
